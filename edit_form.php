/**
 * Form for editing a Related Sections block
 *
 * @copyright 2012 Ian Wild
 * @license   http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class block_related_sections_edit_form extends block_edit_form {
    /**
     * @param MoodleQuickForm $mform
     */
    protected function specific_definition($mform) {
        global $CFG;
        $mform->addElement('header', 'configheader', get_string('blocksettings', 'block'));

        $mform->addElement('text', 'config_title', get_string('title', 'block_related_sections'));
        $mform->setDefault('config_title', get_string('pluginname', 'block_related_sections'));
        $mform->setType('config_title', PARAM_MULTILANG);
    }
}